							<p>With the help of one of AIT’s Financial Aid Advisors, securing funding for training is as simple as answering a few questions. In addition to other aid options, there are several Student Financial Assistance Programs AIT is approved to participate in. An Advisor will match an applicant’s needs to the right program.</p> 
							
							<p><b>Federal Pell Grant:</b> The Federal Pell Grant is needs-based assistance for attending a post-secondary institution. It is not a loan and does not need to be repaid</p>

<p><b>Federal Direct Subsidized Loan:</b>  This is a needs-based loan program started to enable trainees to secure long term loans to attend a post-secondary institution. No interest accrues during enrollment in training and the six month grace period after graduation. Repayment installments usually begin six months after the trainee graduates.</p>

<p><b>Federal Direct Unsubsidized Loan:</b>  This program is for middle-income borrowers and is not needs-based. Interest accrual begins upon the first loan disbursement, but loan repayment does not begin until the six month grace period ends.</p>

<p><b>Federal Direct PLUS Loan:</b>  This program provides additional funds for educational expenses and allows credit-worthy parents of dependent trainees to borrow an amount equal to the cost of attendance minus any other aid received. It accrues interest upon first disbursement and there is no repayment grace period.</p>

<p><b>Private Loan Program:</b>  Credit-worthy trainees, with limited funding through Federal programs and enrolled in the Commercial Truck Driver Program or the Professional Truck Driver Program, may apply for an alternative loan.</p>

<p><b>AIT Institutional Loan Program:</b>  Credit-worthy trainees with limited funding through the above sources may apply for the institutional loan. Repayment begins 30-45 days after completion of the resident portion of the training program.</p>

<p><b>Other Aid Programs:</b>  There may be other funds available for education costs available: Veterans Administration Benefits, Social Security Benefits, Bureau of Indian Affairs, Vocational Rehabilitation Workforce Investment Act, and other scholarships and grants.</p>

<p>Please contact the Financial Aid Department to speak with an Advisor about financial aid, and specific options and eligibility for AIT Truck Driving School.</p>



