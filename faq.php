<div class="content">
<p>Every single one -- gas stations, grocers, retailers, beverage companies, farmers, banks, auto dealerships, hospitals, just to name a few and even things purchased on the internet must be delivered by a truck. The industry has a saying, “If you bought it, a truck brought it.”</p>
