<p class="title" style="left: 0px;"><a href="#">Should I become a truck driver?</a></p>
<div class="content">
<p>The decision to become a truck driver should be an informed one, as it isn’t for everyone. There is a great need in the transportation industry for qualified drivers and new positions continue to open, providing employment security. AIT’s in-depth training options cover vital topics such as impact on family life, time management, and managing work &amp; personal finances. With open eyes and solid training to rely on, a career in trucking may very well be the right choice for you.</p>
