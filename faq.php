<section class="section">
<p class="title" style="left: 0px;"><a href="#">How much money can I make as a truck driver?</a></p>
<div class="content">
<p>Pay and benefits are important issues when considering a new career. Pay scales vary among carriers, of course, but it is reasonable to project that a rookie driver in the first year can earn $40,900<span class="asterisk">*</span> and that’s not including the generous benefits many employing carriers offer. Benefits also vary among carriers, but many offer health &amp; dental insurance, retirement plans, paid time off, wellness programs, and even stock options.</p>
<p>This information was supplied by the carriers that employ the majority of our graduates - Werner, Swift, and Covenant</p>
</div>
</section>
<section class="section">
<p class="title" style="left: 0px;"><a href="#">Are there jobs in trucking in a slow economy?</a></p>
<div class="content">
<p>Absolutely! Even in a sluggish economy or in a recession, truck drivers are essential to every major industry. Every point in production -- from acquiring raw materials, to manufacturing, and on to storage or ultimate delivery of goods -- relies on dependable, regular deliveries. There’s a saying in the industry that rings true, no matter the state of the economy -- "If you bought it, a truck brought it."</p>
</div>
</section>
<section class="section">
<p class="title" style="left: 0px;"><a href="#">What kind of jobs can I find in the industry?</a></p>
<div class="content">
<p>A variety of trucking jobs can be found industry-wide. There are long haul or over the road (OTR) drivers covering long distance routes. LTL drivers, or less than truckload, pick up varying smaller sized loads that ship like full truckloads, but end up delivered much like parcels. There are also countless local route drivers who do a lot of city driving. There is also plenty of room for advancement within the industry -- recruiters, dispatchers, trainers, and even safety &amp; compliance officers start out as drivers to gain valuable experience.</p>
</div>
</section>
<section class="section">
<p class="title" style="left: 0px;"><a href="#">How much does training cost?</a></p>
<div class="content">
<p>Training costs depend greatly on several factors. The program you pick that will provide you the training outcome you are looking for, if you intend to train full time or part time, and what best fits your family's needs.</p>
</div>
</section>
<section class="section">
<p class="title" style="left: 0px;"><a href="#">What is a pre-hire letter?</a></p>
<div class="content">
<p>Employing carriers often give applicants to AIT what’s known as a pre-hire letter. In basic terms, it is an agreement to offer a trainee a permanent position within the company once training is satisfactorily completed.</p>
</div>
</section>
<section class="section">
<p class="title" style="left: 0px;"><a href="#">How prepared will I be after training?</a></p>
<div class="content">
<p>Upon successfully completing any of our programs you will have attained your CDL and be qualified to work as a driver trainee or co-driver. With more extensive training and an externship, you may even be prepared to work in an unsupervised solo driving position. Before you begin your training, it will be clear what sorts of positions you will be prepared to fill upon program completion.</p>
</div>
</section>
<section class="section">
